package com.btcag.bootcamp.PowerUps;

public class ShieldPowerup {

    private int x = 0;
    private int y = 0;
    private final String avatar = "(S)";







    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }

    public String getAvatar() {
        return avatar;
    }


    public void setY(int y) {
        this.y = y;
    }

    public void setX(int x) {
        this.x = x;
    }
}
